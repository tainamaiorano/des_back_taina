// Cria uma variável com uma string
let originalString = "JavaScript é horrível";

// Converte a string para maiúsculas
let upperCaseString = originalString.toUpperCase();

// Converte a string para minúsculas
let lowerCaseString = originalString.toLowerCase();

// Extrai uma parte da string (do índice 0 ao 10)
let slicedString = originalString.slice(0, 10);

// Substitui uma parte da string
let replacedString = originalString.replace("horrível", "fantástico");

// Exibe os resultados no console
console.log("String original:", originalString);
console.log("String em maiúsculas:", upperCaseString);
console.log("String em minúsculas:", lowerCaseString);
console.log("Parte da string extraída:", slicedString);
console.log("String com substituição:", replacedString);
