// Cria uma variável numérica
let numero = 123;

// Cria uma variável string
let texto = "456";

// Converte a variável numérica para string
let numeroComoString = numero.toString();

// Converte a variável string para número
let textoComoNumero = Number(texto);

// Exibe os tipos de dados resultantes no console
console.log("Tipo de dado da variável número convertido para string:", typeof numeroComoString);
console.log("Tipo de dado da variável texto convertido para número:", typeof textoComoNumero);
