// Declara duas variáveis booleanas
let condicao1 = false;  // Primeira condição
let condicao2 = true;   // Segunda condição

// Usa o operador OR para verificar se pelo menos uma das condições é verdadeira
let resultado = condicao1 || condicao2;

// Exibe o resultado no console
if (resultado) {
    console.log("Pelo menos uma das condições é verdadeira.");
} else {
    console.log("Nenhuma das condições é verdadeira.");
}
